function getUrlImgS22(srcImgS22, corCelularS22) {

    let s_22 = document.getElementById('s22')

    let celular_s22 = document.getElementById('celular_s22')
    
    s_22.src = `../imagens/s22/${srcImgS22}`

    celular_s22.style.background = corCelularS22
    
}

